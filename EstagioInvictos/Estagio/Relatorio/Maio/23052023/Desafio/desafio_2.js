let PI = 3.14;
let raio = 10;
let areaCiscuferencia = PI * raio * raio;

console.log("A area de circunferencia e igual a: " 
    + areaCiscuferencia);

// Ou pode ser usado o PI de forma mais e exata, usando o "Math."

areaCiscuferencia = Math.PI * raio * raio;
console.log("A area de circunferencia e igual a: " 
    + areaCiscuferencia);
